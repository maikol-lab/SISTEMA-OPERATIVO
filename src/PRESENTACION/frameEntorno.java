public void invocar(String cadena){
    int nMarcas = 3;
    String[] numeros = new String[ cadena.length() ];
    for (int i = 0; i < numeros.length; i++) {
        numeros[i] = cadena.substring(i, i + 1);
    }
    LRU lru = new LRU( nMarcas, numeros );
    String [][] paginas = lru.procesar();
    administrador.graficarPaginas(paginas,lru.getNFallos());
}
